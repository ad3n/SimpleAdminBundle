class RequestResponseWithEntityEvent extends EntityResponseEvent
{
    protected $request;

    public function setRequest(Request $request)
    {
        $this->request = $request;
    }

    /**
     * @return Request
     */
    public function getRequest()
    {
        return $this->request;
    }
}
